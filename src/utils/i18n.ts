export const i18n = {
  getCurrent(): Language {
    return (localStorage.getItem('skilltok_lang') as Language) || 'en';
  },
  set(lang: Language) {
    localStorage.setItem('skilltok_lang', lang);
    window.dispatchEvent(new Event('languagechange'));
  },
  t(key: string): string {
    const lang = this.getCurrent();
    return translations[lang]?.[key] || translations.en[key] || key;
  },
};
